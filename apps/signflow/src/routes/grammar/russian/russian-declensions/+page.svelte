    <h3>Key Points about Russian Declensions</h3>
    <p>
      <strong
        >1. Cases indicate grammatical function:</strong
      >
    </p>
    <div class="case-explanation">
      <p>
        <strong>Nominative:</strong> Subject of the sentence (who/what
        is doing the action)
      </p>
      <p>
        <strong>Genitive:</strong> Possession, absence, after
        certain prepositions and numbers
      </p>
      <p>
        <strong>Dative:</strong> Indirect object (to/for whom),
        after certain verbs
      </p>
      <p>
        <strong>Accusative:</strong> Direct object (whom/what
        the action affects), after certain prepositions
      </p>
      <p>
        <strong>Instrumental:</strong> Means by which action is
        done (with what), after certain prepositions
      </p>
      <p>
        <strong>Prepositional:</strong> Always used with prepositions
        (about, in, on)
      </p>
    </div>
    <p>
      <strong>2. Gender matters:</strong> Masculine, feminine,
      and neuter nouns have different declension patterns.
    </p>
    <p>
