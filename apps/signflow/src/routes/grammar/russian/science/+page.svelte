  <section id="spelling">
    <h2>Russian Spelling Rules</h2>
    <div class="section-container">
      <div class="card">
        <div class="card-header">
          <div class="icon">✍️</div>
          <h3>Basic Spelling Rules</h3>
        </div>
        <ul>
          <li>
            After <span class="highlight"
              >Ж, Ш, Ч, Щ, Ц</span
            >, write <span class="highlight">И</span>
            instead of <span class="highlight">Ы</span>
          </li>
          <li>
            After <span class="highlight"
              >Ж, Ш, Ч, Щ, Ц</span
            >, write <span class="highlight">А</span>
            instead of <span class="highlight">Я</span>
          </li>
          <li>
            After <span class="highlight"
              >Ж, Ш, Ч, Щ, Ц</span
            >, write <span class="highlight">У</span>
            instead of <span class="highlight">Ю</span>
          </li>
          <li>
            Write <span class="highlight">О</span> under
            stress, <span class="highlight">А</span> without stress
            in some roots
          </li>
          <li>
            <span class="highlight">Ь</span> soft sign palatalizes
            preceding consonant
          </li>
        </ul>
      </div>

      <div class="card">
        <div class="card-header">
          <div class="icon">🔤</div>
          <h3>Vowel Reduction</h3>
        </div>
        <ul>
          <li>
            Unstressed <span class="highlight">О</span>
            sounds like <span class="highlight">А</span>
          </li>
          <li>
            Unstressed <span class="highlight">Е</span>
            sounds like <span class="highlight">И</span>
          </li>
          <li>
            Unstressed <span class="highlight">Я</span>
            sounds like <span class="highlight">И</span>
          </li>
          <li>This is called "аканье" (akan'ye)</li>
        </ul>
      </div>

      <div class="card">
        <div class="card-header">
          <div class="icon">🔄</div>
          <h3>Consonant Assimilation</h3>
        </div>
        <ul>
          <li>
            Voiced consonants become voiceless at word end
          </li>
          <li>
            Voiceless consonants become voiced before voiced
            consonants
          </li>
          <li>
            <span class="highlight">Г</span> often becomes
            <span class="highlight">В</span> in endings
          </li>
          <li>
            Consonant clusters simplify in pronunciation
          </li>
        </ul>
      </div>
    </div>
  </section>

