  <div class="notes">
    <h3>Notes on Arabic Conjugation:</h3>
    <ul>
      <li>
        <strong>Prefixes</strong> (blue) indicate the subject
        in present, subjunctive, and jussive tenses.
      </li>
      <li>
        <strong>Suffixes</strong> (red) indicate the subject in
        past tense and add gender/number information in other
        tenses.
      </li>
      <li>
        <strong>Verb root</strong> (green) remains relatively
        constant across conjugations.
      </li>
      <li>
        <strong>Pronouns</strong> (orange) are often omitted in
        Arabic sentences since the verb conjugation already indicates
        the subject.
      </li>
      <li>
        The subjunctive tense is used after certain
        particles (like أن - "that/to") to express purpose,
        desire, or necessity.
      </li>
      <li>
        The jussive tense is used in commands, prohibitions,
        and after certain particles (like لم - "did not").
      </li>
    </ul>
